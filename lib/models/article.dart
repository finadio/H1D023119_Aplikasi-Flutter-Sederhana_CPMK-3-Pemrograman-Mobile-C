class Article {
  final int id;
  final String title;
  final String author;
  final String date;
  final String category;
  final String image;
  final String excerpt;
  final String content;

  Article({
    required this.id,
    required this.title,
    required this.author,
    required this.date,
    required this.category,
    required this.image,
    required this.excerpt,
    required this.content,
  });
}
